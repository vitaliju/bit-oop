export const matematic = {
    add: (a, b) => a + b,
    diff: (a, b) => a - b,
    multi: (a, b) => a * b,
    div: (a, b) => a / b,
    mod: (a, b) => a % b,
};


console.log(matematic.add(5, 6));
console.log(matematic.diff(5, 6));
console.log(matematic.multi(5, 6));
console.log(matematic.div(5, 6));
console.log(matematic.mod(5, 6));